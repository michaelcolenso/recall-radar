#!/usr/bin/env node
// Extracts the @critical-marked regions from public/styles.css and writes
// src/templates/critical-css.ts for inlining in <head>.
// Usage: node scripts/build-critical-css.mjs

import { readFileSync, writeFileSync } from "node:fs";
import { fileURLToPath } from "node:url";
import path from "node:path";

const CRITICAL_REGION = /\/\* @critical:start \*\/([\s\S]*?)\/\* @critical:end \*\//g;

export function extractCriticalCss(cssText) {
  const regions = [];
  for (const match of cssText.matchAll(CRITICAL_REGION)) {
    regions.push(match[1]);
  }
  return regions
    .join("\n")
    .replace(/\/\*[\s\S]*?\*\//g, "")
    .replace(/\s+/g, " ")
    .trim();
}

const isMain = process.argv[1] && fileURLToPath(import.meta.url) === path.resolve(process.argv[1]);

if (isMain) {
  const root = path.resolve(fileURLToPath(import.meta.url), "../..");
  const cssPath = path.join(root, "public/styles.css");
  const outPath = path.join(root, "src/templates/critical-css.ts");

  const css = readFileSync(cssPath, "utf-8");
  const critical = extractCriticalCss(css);

  const output =
    `// AUTO-GENERATED by scripts/build-critical-css.mjs — DO NOT EDIT.\n` +
    `// Run \`npm run build:critical\` after editing the @critical regions in public/styles.css.\n` +
    `export const CRITICAL_CSS = ${JSON.stringify(critical)};\n`;

  writeFileSync(outPath, output);
  console.log(`Wrote ${critical.length} chars of critical CSS to ${path.relative(root, outPath)}`);
}
